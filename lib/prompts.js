import { BRANDS, fmtDateFull, dowHeb, diffDays } from "./core";

const PROFILES = {
  WN: "WINE NOT — פסטיבל יין. קהל 22–35, ותיקה וגדולה. טון: חגיגי ותוסס.",
  MX: "MIXIT — פסטיבל קוקטיילים. קהל 30+, בוגר. טון: מתוחכם ועדין, לא רועש.",
  BG: "BAGLIL — פסטיבלי יין ומסיבות בגליל. קהל מעורבב, קהילה נאמנה. טון: אותנטי, גלילי וחם.",
  WB: "WINE NOT BAR — בר יין בבנימינה, פיילוט, ימי חמישי. טון: אינטימי, מסקרן, סיפור פתיחה.",
};

export function buildGeminiPrompt(event) {
  const profile = PROFILES[event.brand] || "";
  const link = event.link || "(אין קישור — אל תמציא, השאר מקום ריק)";
  const today = new Date(); today.setHours(0,0,0,0);
  const daysOut = diffDays(today, new Date(event.date));

  return `אתה עוזר לי לכתוב הודעות קהילה לווטסאפ עבור חברת הפקות אירועים בשם A&A HAFAKOT.

ההפקה הרלוונטית:
${profile}

פרטי האירוע:
- שם: ${event.name}
- תאריך: ${fmtDateFull(event.date)} (יום ${dowHeb(event.date)})
- מיקום: ${event.location || "(לא צוין)"}
- קישור לכרטיסים: ${link}
- מרחק מהיום: ${daysOut >= 0 ? daysOut + " ימים לאירוע" : "האירוע עבר"}

חוקי כתיבה וסגנון קבועים:
- עברית מלאה.
- כותרת: כל הודעה נפתחת בכותרת קצרה ומודגשת עם אימוג'י רלוונטי בסופה.
- מבנה וריווח: פסקאות קצרות ואווריריות (שורה-שתיים) עם רווח (אנטר) ביניהן. לא לחפור.
- הדגשות: להדגיש (Bold) פרטים חשובים כמו שמות אמנים מיוחדים או הטבות (ללא הגבלה / חופשי).
- טון: מקצועי אבל חם, כמו חבר שיודע על מה הוא מדבר.
- פתיחה: בלי "ברוכים הבאים", בלי הצגות, בלי "היי" גנרי. מתחילים ישר ולעניין.
- סיום: הנעה לפעולה אלגנטית, אימוג'י מצביע מטה (👇🏼) ומיד אחריו הקישור.

המשימה:
תייצר **שתי הודעות קהילה** לאירוע הזה:

הודעה #1 — רחוקים מהאירוע (ערך/טיזר, נשלח כ-10–14 יום לפני):
נותנת הצצה, פרט מיוחד, סיפור קטן. מחממת. מסיימת ברמיזה, בלי קריאה ישירה לקנות.

הודעה #2 — קרובים לאירוע (CTA, נשלח כ-2–3 ימים לפני):
ישירה, פרטים מעשיים — מתי, איפה, לינק. דחיפות בלי אגרסיביות.

פורמט התשובה:
═══ הודעה #1 · ערך/טיזר ═══
[ההודעה]

═══ הודעה #2 · CTA ═══
[ההודעה]

בלי הקדמות, בלי הסברים. רק שתי ההודעות.`;
}
