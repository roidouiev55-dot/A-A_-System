import { BRANDS, fmtDateFull, dowHeb, diffDays } from "./core";

const PROFILES = {
  WN: "WINE NOT — פסטיבל יין ותיק וגדול. קהל 22–35. טון: חגיגי, תוסס, מזמין.",
  MX: "MIXIT — פסטיבל קוקטיילים. קהל 30+ בוגר ומתוחכם. טון: עדין, אלגנטי, לא רועש.",
  BG: "BAGLIL — פסטיבלי יין ומסיבות בגליל. קהל מעורבב ונאמן. טון: אותנטי, גלילי, חם ומחבר.",
  WB: "WINE NOT BAR — בר יין בבנימינה, פיילוט, ימי חמישי. טון: אינטימי, מסקרן, סיפור פתיחה.",
};

// msgType: "ערך" (far from event) or "CTA" (close to event)
export function buildGeminiPrompt(event, msgType) {
  const profile = PROFILES[event.brand] || "";
  const link = event.link || "(אין קישור — אל תכתוב לינק מומצא)";
  const today = new Date(); today.setHours(0,0,0,0);
  const daysOut = diffDays(today, new Date(event.date));
  const isValue = msgType === "ערך";

  const typeBlock = isValue
    ? `סוג ההודעה: הודעת ערך / טיזר (נשלחת מוקדם, כשעוד רחוקים מהאירוע).
המטרה: לחמם, לסקרן, לתת ערך. הצצה / פרט מיוחד / סיפור קצר על מה שמחכה.
לא למכור באגרסיביות. לסיים ברמיזה למה שמגיע, לא בקריאה לקנות עכשיו.`
    : `סוג ההודעה: הודעת CTA (נשלחת קרוב לאירוע).
המטרה: להניע לפעולה. פרטים מעשיים — מתי, איפה, ולמה כדאי למהר. דחיפות אלגנטית בלי לחץ.
לסיים בקריאה ברורה לפעולה עם אימוג'י 👇🏼 והקישור.`;

  return `אתה קופירייטר מקצועי שכותב הודעות ווטסאפ לקהילות של חברת הפקות אירועים בשם A&A HAFAKOT.

ההפקה:
${profile}

פרטי האירוע:
- שם: ${event.name}
- תאריך: ${fmtDateFull(event.date)} (יום ${dowHeb(event.date)})
- מיקום: ${event.location || "(לא צוין)"}
- קישור לכרטיסים: ${link}
- מרחק מהיום: ${daysOut >= 0 ? daysOut + " ימים לאירוע" : "האירוע עבר"}

${typeBlock}

חוקי כתיבה (חובה):
- עברית מלאה, כתיבה חיה וזורמת — לא רובוטית.
- פתיחה: כותרת קצרה ומודגשת עם אימוג'י רלוונטי בסופה. בלי "ברוכים הבאים", בלי "היי" גנרי. ישר ללב.
- מבנה: פסקאות קצרות ואווריריות (שורה-שתיים), עם שורת רווח ביניהן. לא לחפור.
- הדגשות: הדגש (בכוכביות) פרטים חשובים — שמות אמנים, הטבות, תאריך.
- טון: מקצועי אבל חם, כמו חבר שמבין עניין ומזמין אותך.
- אורך: בינוני — מספיק כדי לרגש, קצר מספיק כדי שיקראו עד הסוף.
- אל תמציא פרטים שלא נתתי (אמנים, מחירים). אם חסר מידע — אל תכתוב אותו.

חשוב: תחזיר אך ורק את ההודעה עצמה, מוכנה להעתקה ישירה לווטסאפ. בלי כותרת "הנה ההודעה", בלי הסברים, בלי הערות. רק ההודעה.`;
}
